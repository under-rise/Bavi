                    <h4 style="margin-bottom: 10px;">利用者と施設をマッチングするプラットフォーム</h4>
                    <p class="box">Baviは日本全国のバーベキュ施設を紹介し、<span class="line">利用者と施設をマッチングするプラットフォーム</span>です。これにより施設は集客を最大化し、利用者は自分好みの施設や会場を探すことができます。また、<span class="line"> 施設側は案件管理や商材支援、広告、利益改善を行うことができ、最小限のコストで最大限の利益を生み出す</span>ことが可能となります。<br>
                        発展系として、施設の近隣の食材店舗やスーパー、レンタカー会社、BBQ後の二次会や温泉、お風呂施設、ホテル、アウトドアに関連する事業者との連携、広告など様々な店舗と連携が可能となります。BBQ施設関連事業者、地域事業者、地域活性化にも一役かえることになります。<br>
                        <span class="line">利用者はバーベキュー施設を探しやすくなること、利用すればするほど貯まるポイントを利用し商品や現金に換金することも可能</span>となり、有料会員ともなれば、お小遣い稼ぎのツールとしても利用ができるようになります。将来的にはゲーム、漫画、SNSのコンテンツを導入し、ユーザーの利便性を高めることができます。
